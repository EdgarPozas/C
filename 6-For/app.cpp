//	ciclo for
#include <iostream>

using namespace std;

int main(int argc, char const *argv[])
{
	/*
		si queremos realizar instrucciones por repetidas veces
		usamos ciclos
		usaremos el ciclo for
	*/
	//este ciclo va desde donde empiza,hasta donde va a llegar y cuanto va a ir aumentando
	for (int i = 0; i <=10; i++)
	{
		cout<<"Vez en el ciclo "<< i <<endl;
	}
	cin.get();
	return 0;
}
